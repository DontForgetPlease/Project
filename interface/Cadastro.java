    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
     Usuario user = new Usuario();
     DAOUsuario conexao = new DAOUsuario();
     try{
         if(conexao.isExiste(jEmail.getText())){
             throw new emailExistente();
         }
         user.setEmail(jEmail.getText());
         user.setSenha(jSenha.getText());
         user.setNome(jNome.getText());
         conexao.criarUsuario(user);
         VoltarLogin v = new VoltarLogin();
         v.setVisible(true);
         this.dispose();
         
     }catch(NumeroCaracterInvalido ex){
         TatamentoDeErro tr = new TatamentoDeErro();
         tr.erro.setText("Numero Caracter Invalido");
         tr.setVisible(true);
         this.dispose();
     }catch(emailExistente ex){
         TatamentoDeErro tr = new TatamentoDeErro();
         tr.erro.setText("Email Inv√°lido");
         tr.setVisible(true);
         this.dispose();
     }
    }//GEN-LAST:event_jButton1ActionPerformed

